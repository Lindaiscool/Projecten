Amsterdam afrit 1, Oudekerk a/d Amstel/Duivendrecht nemen en aan het einde van de afrit linksaf richting Amsterdam
Zuidoost/Duivendrecht volgen (burg. Stramanweg).<br>

<br><b>Vanuit de richting Zaandam/Den haag:</b><br>
